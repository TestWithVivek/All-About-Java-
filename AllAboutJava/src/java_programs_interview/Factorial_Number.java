package java_programs_interview;

public class Factorial_Number {

	public static void main(String[] args) {
		
	//	Factorial - 5 = 5x4x3x2x1
		
		int number=5;
		 
		int fact = 1;
		
		for(int i=1;i<=number;i++) {
			
			fact = fact*i;
			
			
			System.out.println(fact);
			
			
		}
		
		
		
		
		
		
	}
	
	
	
	
}
