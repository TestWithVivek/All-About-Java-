package basic.of.java;

public class Looping_While_Loop {

//the same line of code executes untill the condition get satisfied 
	
	
	public static void main(String[] args) {
		
		int i=0;       // we have to decalir the value of i;
		               // also it will execute only when the condition is true
		
		while(i<10){
			
			System.out.println(i);
			 
		
			i++;
		}
		
		
	}
	
	
	
	
}
